from statistics import avg

print('5, 10, 4, 9, 30, 16, 2, 11')
print('Rata-rata = ', avg(5, 10, 4, 9, 30, 16, 2, 11))
print('Maksimum  = ', max(5, 10, 4, 9, 30, 16, 2, 11))
print('Minimum   = ', min(5, 10, 4, 9, 30, 16, 2, 11))

print('\n81, 98, 12, 83, 45, 77, 69, 30, 56')
